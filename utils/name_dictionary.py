def get_name_pronunciation(name):

    name_lower = name.lower().strip()
    return NAME_PRONUNCIATIONS.get(name_lower, name)

def convert_names_in_text(text):

    words = text.split()
    converted_words = []

    for word in words:

        clean_word = word.strip('.,!?;:')

        if clean_word and clean_word[0].isupper():

            phonetic = get_name_pronunciation(clean_word)

            if word != clean_word:
                punctuation = word[len(clean_word):]
                converted_words.append(phonetic + punctuation)
            else:
                converted_words.append(phonetic)
        else:
            converted_words.append(word)

    return ' '.join(converted_words)

def is_name(word):

    return word.lower().strip('.,!?;:') in NAME_PRONUNCIATIONS
